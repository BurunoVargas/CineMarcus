    public void carregarFilmes()
    {
        cmbFilme.removeAllItems();
        
        cmbFilme.addItem("Selecione um Filme");
        
        for (int i = 0; i < listaFilmes.size(); i++) {
            cmbFilme.addItem(listaFilmes.get(i).getNome());
        }
    }
    
