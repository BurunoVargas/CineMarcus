    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nomeDeUsuario = txtNomeDeUsuario.getText();
        String email = txtEmail.getText();
        String senha = txtSenha.getText();
        try {
            int idade = Integer.parseInt( txtIdade.getText());
            if (idade <= 0) {
                throw new NumberFormatException();
            }
            
        if(nomeDeUsuario.equals("") || email.equals("") || senha.equals("") || txtIdade.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Todos os campos de cadastro devem estar preenchidos");
        }else if(login.existeContaNome(nomeDeUsuario)) {
            JOptionPane.showMessageDialog(null, "J치 existe uma conta com esse nome de usu치rio");
            txtNomeDeUsuario.setText("");
            txtNomeDeUsuario.requestFocus();

        }else if(login.existeContaEmail(email)) {
            JOptionPane.showMessageDialog(null, "J치 existe uma conta com esse email");
            txtEmail.setText("");
            txtEmail.requestFocus();

        }else {
            Pessoa novoUsuario = new Cliente(0, idade, email, senha, nomeDeUsuario);
            login.add(novoUsuario);
            JOptionPane.showMessageDialog(null, "Conta criada com sucesso");
            dispose();

        }
            
            
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Insira um valor v치lido", "Erro", JOptionPane.ERROR_MESSAGE);
        }        
    }//GEN-LAST:event_btnCadastrarActionPerformed

